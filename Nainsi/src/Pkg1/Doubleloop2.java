import java.util.Scanner;

public class Doubleloop2 {
	
	public static void main (String[] args)
	
	{
         int i;
		for ( i=6; i>=1 ; i--)
		{
		  for ( int j=1; j<=i ; j++)
			{
		    	System.out.print("*");
		    }
		   System.out.println();
		}
		
		
	}
	}
